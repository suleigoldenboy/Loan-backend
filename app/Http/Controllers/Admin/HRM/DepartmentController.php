     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Department $department, DepartmentRequest $request)
    {
        try {
            $department->create($request->all());
            return appRedirect([], 'department.index', ['successMessage', message('response.department.create')], $request);
        } catch (\Throwable $th) {
            $error = $th->getMessage();
            $request->session()->flash('errorMessage', $error.message('response.error500'));
            return redirect()->route('department.create');
        }
    }

